#include <stdio.h>

int main()
 {
    printf("Integer Equivalents:\n");

    printf("Uppercase Letters:\n");
    printf("A: %d\n", 'A');
    printf("B: %d\n", 'B');
    printf("C: %d\n", 'C');

    printf("Lowercase Letters:\n");
    printf("a: %d\n", 'a');
    printf("b: %d\n", 'b');
    printf("c: %d\n", 'c');

    printf("Digits:\n");
    printf("0: %d\n", '0');
    printf("1: %d\n", '1');
    printf("2: %d\n", '2');

    printf("Special Symbols:\n");
    printf("$: %d\n", '$');
    printf("*: %d\n", '*');
    printf("+: %d\n", '+');
    printf("/: %d\n", '/');
    printf("Blank Character: %d\n", ' ');

    return 0;
}


